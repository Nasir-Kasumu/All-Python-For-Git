Grade = str(input("What is your letter grade?(in uppercase with no + or -)"))

if Grade == "A":
    print("Your grade 4.0")
elif Grade == "B":
    print("Your grade 3.0")
elif Grade == "C":
    print("Your grade is 2.0")
elif Grade == "D":
    print("Your grade is 1.0")
elif Grade == "F":
    print("Your grade is 0")
